        AlarmCheck AT = new AlarmCheck();
        String ATime = jTextField2.getText();
        String [] ar = ATime.split(":");
        int ah = Integer.parseInt(ar[0].trim()) ;
        int am = Integer.parseInt(ar[1].trim()) ;
        int as = Integer.parseInt(ar[2].trim()) ;
        AT.setAlarm(ah, am, as);
        String inter = jTextField3.getText();
        ar = inter.split(":");
        int inM = Integer.parseInt(ar[0].trim());
        int inS = Integer.parseInt(ar[1].trim());
        AT.setInteval(inM, inS);
        String rep = jComboBox1.getSelectedItem().toString();
        int repeat = 0;
        if (rep.equalsIgnoreCase("1 time")) {
            repeat = 1;
        }
        if (rep.equalsIgnoreCase("2 times")) {
            repeat = 2;
        }
        if (rep.equalsIgnoreCase("3 times")) {
            repeat = 3;
        }
        AT.setRepeat(repeat);
        try {
            AT.doAlarm();
        } catch (InterruptedException ex) {
            Logger.getLogger(Alarm.class.getName()).log(Level.SEVERE, null, ex);
        }
