    class TimeThread extends Thread {

        @Override
        public void run() {
            try {
                while(true) {
                    Calendar c = Calendar.getInstance();
                    int insH = c.get(Calendar.HOUR);
                    int insM = c.get(Calendar.MINUTE);
                    int insS = c.get(Calendar.SECOND);
                    String now = insH + ":" + insM + ":" + insS;
                    jTextField1.setText(now);
                    Thread.sleep(1000);
                }
            }catch (Exception e) {
                System.out.println(e);
            }
        }
        
    }
    
    class AlarmCheck {
        
        int ah, am, as, inM, inS, rept;

        public AlarmCheck() {
            ah = am = as = inM = inS = rept = -1;
        }
        public void setAlarm(int h, int m, int s) {
            ah = h;
            am = m;
            as = s;
        }
        
        public void setInteval(int m, int s) {
            inM = m;
            inS = s;
        }
        
        public void setRepeat(int re) {
            rept = re;
        }
        
        
        public void doAlarm() throws InterruptedException {
            int count = 0;
            while(count != rept) {
                jButton1.setEnabled(false); 
                Calendar c = Calendar.getInstance();
                int insH = c.get(Calendar.HOUR);
                int insM = c.get(Calendar.MINUTE);
                int insS = c.get(Calendar.SECOND);
                
                if (insH == ah && insM == am && insS == as) {
                    String now = insH + ":" + insM + ":" + insS;
                    jTextArea1.append("Good morning ai time: " + now + "\n");
                    ah = (ah + (am + inM + (as + inS)/60)/60) %12;
                    am = (am + inM + (as + inS)/60) %60;
                    as = (as + inS)%60;
                    count ++;
                }
                Thread.sleep(1000);
            }
            jButton1.setEnabled(true);
        }    
    }
    
