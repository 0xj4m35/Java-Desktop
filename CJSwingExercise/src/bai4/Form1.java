    void moveLeft() {
        String s = jLabel1.getText();
        char c = s.charAt(0);
        s = s.substring(1);
        s = s + c;
        jLabel1.setText(s);
    }
 
    void moveRight() {
        String s = jLabel1.getText();
        int len = s.length();
        char c = s.charAt(len-1);
        s = c + s.substring(0, len-1);
        jLabel1.setText(s);
    }
    
    int direction = 0; // 1 - left ; 2 - right
    
    class TextThread extends Thread {

        @Override
        public void run() {
            try {
                while(true) {
                    if (direction == 1) {
                        moveLeft();
                    }
                    else if (direction == 2) {
                        moveRight();
                    }
                    sleep(500);
                }
            } catch (Exception e) {
                System.out.println(e);
            }
        }
        
    }
    
    TextThread tt;
    
    public Form1() {
        initComponents();
        tt = new TextThread();
        tt.start();
    }

    
   
