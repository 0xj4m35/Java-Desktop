    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        MainDialog md = new MainDialog(this, true);
        md.setVisible(true);
        
        String pathFile = md.getFilePath();
        boolean isDisplaySize = md.isDisplaySize();
        boolean isDisplayDate = md.isDisplayDate();
        
        File f = new File(pathFile);
        if (!f.exists()) {
            JOptionPane.showMessageDialog(this, "File not found.");
        }
        else {
            String txt = "";
            if (isDisplaySize) {
                txt += "Size of file: " + f.length() + " bytes.\n" ;
            }
            if (isDisplayDate) {
                Date d = new Date();
                DateFormat dfm = new SimpleDateFormat("dd/MM/yyyy");              
                txt += "Today is : " + dfm.format(d) + "\n";
            }
            txt += getContentFile(f);
            jTextArea1.setText(txt);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

