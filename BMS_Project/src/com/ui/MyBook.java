    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ArrayList<BookData> listFound = new ArrayList<>();
        String keyword = jTextField1.getText().trim();
        String searchBy = jComboBox1.getSelectedItem().toString();
        int type = 1;
        if (searchBy.equalsIgnoreCase("Book ID")) {
            type = 1;
        }
        else if (searchBy.equalsIgnoreCase("Book Title")) {
            type = 2;
        }
        else if (searchBy.equalsIgnoreCase("Publisher Name")) {
            type = 3;
        }
        else if (searchBy.equalsIgnoreCase("Author Name")) {
            type = 4;
        }
        else if (searchBy.equalsIgnoreCase("Notes")) {
            type = 5;
        }
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Blank input. Enter again.");
            return;
        }
        else {
            try {
                 listFound = new BookController().getListFound(u, keyword, type);
            } catch (Exception e) {
                System.out.println(e);
            }
        }
        displayBook(listFound);
    }//GEN-LAST:event_jButton1ActionPerformed

