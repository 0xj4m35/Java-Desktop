    public void displayBook(ArrayList<BookData> listBookData) {
        DefaultTableModel tbModel = (DefaultTableModel)jTable1.getModel();
        tbModel.setRowCount(0);
        for (BookData b : listBookData) {
            String titID = b.getTitle_id();
            String title = b.getTitle();
            String pub = b.getPub();
            String aut = b.getAuthor();
            String notes = b.getNotes();
            Object [] data = {titID, title, pub, aut, notes};
            tbModel.addRow(data);
        }
    }
    
