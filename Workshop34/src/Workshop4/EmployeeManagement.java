    public boolean isExistCode(String code) {
        ArrayList<Employee> list = listEmp.getListEmp();
        for (int i = 0; i < list.size(); i++) {
            if (code.equalsIgnoreCase(list.get(i).getCode())) {
                JOptionPane.showMessageDialog(this, "Code has been existed. Please enter again.");
                return true;
            }
        }
        return false;
    }
    
