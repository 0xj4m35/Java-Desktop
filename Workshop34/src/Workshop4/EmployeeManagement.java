    boolean noSpe(String txt) {
        if (txt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Blank input. Please enter again");
            return false;
        }
        else {
            if (!txt.matches("[a-zA-Z0-9]*")) {
                JOptionPane.showMessageDialog(this, "Only Character and Number. Please enter again.");
                return false;
            }
        }
        return true;
    }
    
    boolean noSpeNum(String txt) {
        if (txt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Blank input. Please enter again");
            return false;
        }
        else {
            if (!txt.matches("[a-zA-Z\\s]*")) {
                JOptionPane.showMessageDialog(this, "Only Character and Space. Please enter again.");
                return false;
            }
        }
        return true;
    }
    
    void displayInTable(ArrayList<Employee> list) {
        tbModel.setRowCount(0);
        for (Employee em : list) {
            tbModel.addRow(em.toDataRow());
        }
    }
    
    void calIncome() {
        String sal = jTextField3.getText().trim();
        String bon = jTextField4.getText().trim();
        if (!sal.isEmpty() && !bon.isEmpty()) {
            double sala = Double.parseDouble(sal);
            double bonus = Double.parseDouble(bon);
            double inc = sala + bonus;
            jTextField5.setText(inc + "");
        }
    }
