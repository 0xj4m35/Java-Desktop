    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.setMultiSelectionEnabled(false);
        int returnVal = fc.showOpenDialog(this);
        
        tbModel.setRowCount(0);
        
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File f = fc.getSelectedFile();
            if (f == null) {
                return;
            }
            String fName = f.getName();
            if (fName.endsWith("txt")) {
                try {
                    BufferedReader br = new BufferedReader(new FileReader(f));
                    String line = "";
                    while ((line = br.readLine()) != null) {
                        String [] arr = line.split("[|]");
                        String code = arr[0].trim();
                        String name = arr[1].trim();
                        String sex = arr[2].trim();
                        double sal = Double.parseDouble(arr[3].trim());
                        double bonus = Double.parseDouble(arr[4].trim());
                        Employee em = new Employee(code, name, sex, sal, bonus);
                        listEmp.add(em);
                    }
                    br.close();
                } 
                catch (IOException e) {
                    JOptionPane.showMessageDialog(this, "Open file fault.");
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "OOps. Fault.");
                }
            }
        }
        ArrayList<Employee> empList = listEmp.getListEmp();
        displayInTable(empList);
    }//GEN-LAST:event_jButton1ActionPerformed

